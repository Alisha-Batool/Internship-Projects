import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

"""**Step 2: Load the Iris Dataset**"""

# Load dataset
iris = sns.load_dataset('iris')

# Quick look
iris.head()

"""**Step 3: Check Dataset Details** \
.shape → number of rows and columns.\
.info() → types of columns and missing values.\
.describe() → mean, std, min, max, etc.
"""

# Shape of the dataset
print("Shape:", iris.shape)

# Column names
print("Columns:", iris.columns.tolist())

# Info about data types and nulls
iris.info()

# Summary statistics
iris.describe()

"""**Step 4: Scatter Plots (Feature Relationships)**"""

sns.pairplot(iris, hue='species', markers=["o", "s", "D"])
plt.suptitle("Pairwise Feature Relationships", y=1.02)
plt.show()

"""**Step 5: Histograms (Feature Distributions)**"""

iris.hist(bins=15, figsize=(12, 8), color='skyblue', edgecolor='black')
plt.suptitle("Feature Distributions")
plt.show()

"""**Step 6: Box Plots (Detect Outliers)**"""

plt.figure(figsize=(12, 6))
for i, col in enumerate(iris.columns[:-1], 1):
    plt.subplot(1, 4, i)
    sns.boxplot(y=iris[col], color='lightgreen')
    plt.title(f"Boxplot of {col}")
plt.tight_layout()
plt.show()